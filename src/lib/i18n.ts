let currentLocale: Locale = 'pt';
let listeners: (() => void)[] = [];

export function getLocale(): Locale { return currentLocale; }

export function setLocaleValue(l: Locale) {
  currentLocale = l;
  listeners.forEach(fn => fn());
}

export function translate(key: string): string {
  return translations[currentLocale]?.[key] || translations.pt[key] || key;
}

export function subscribeI18n(fn: () => void) {
  listeners.push(fn);
  return () => { listeners = listeners.filter(l => l !== fn); };
}

export function useI18n() {
  const [, setTick] = useState(0);
  useEffect(() => {
    return subscribeI18n(() => setTick(t => t + 1));
  }, []);
  return { locale: currentLocale, setLocale: setLocaleValue, t: translate };
}
